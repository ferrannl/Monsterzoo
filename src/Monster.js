export class Monster {
    constructor(id, elementType, armType, arms, legs, eyes, furType, canFly, canSwim, color, specialPower, powerLevel, monsterTitle) {
        this.id = id;
        this.elementType = elementType;
        this.armType = armType;
        this.arms = arms;
        this.legs = legs;
        this.eyes = eyes;
        this.furType = furType;
        this.canFly = canFly;
        this.canSwim = canSwim;
        this.color = color;
        this.specialPower = specialPower;
        this.powerLevel = powerLevel;
        this.monsterTitle = monsterTitle;
    }
}